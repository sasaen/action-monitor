var stompClient = null;

function setConnected(connected) {
    $("#connect").prop("disabled", connected);
    $("#disconnect").prop("disabled", !connected);
    if (connected) {
        $("#conversation").show();
    }
    else {
        $("#conversation").hide();
    }
    $("#messages").html("");
}

function connect() {
	// TODO update localhost with the server name
    var socket = new SockJS('http://localhost:8080/database-websocket');
        
    //console.dir(socket)
    
    stompClient = Stomp.over(socket);
    stompClient.connect({}, function (frame) {
        setConnected(true);
        console.log('Connected: ' + frame);
        stompClient.subscribe('/topic/database', function (databaseMessage) {        	        	
        	showMessage(JSON.parse(databaseMessage.body).content);
        });
    });
}

function disconnect() {
    if (stompClient != null) {
        stompClient.disconnect();
    }
    setConnected(false);
    console.log("Disconnected");
}


function showMessage(message) {
    $("#messages").append("<tr><td>" + message + "</td></tr>");
}

